CREATE OR REPLACE PROCEDURE 
P_CTM_ALTERARLOTE_PERM (P_USUARIO      IN VARCHAR2,
				   P_EXIST        IN OUT INTEGER) IS 

V_COUNT NUMBER;
BEGIN
	SELECT COUNT(*)
	  INTO V_COUNT
	  FROM LF.LF_USUARIO_FUNCOES
	 WHERE MODULO = 'ALTERAREMLOTE'
	   AND COD_USUARIO = P_USUARIO;	

	
	IF (V_COUNT > 0) THEN
		P_EXIST := 1;
	ELSE
		P_EXIST := 0;
	END IF; 	
END;
/
GRANT EXECUTE ON  P_CTM_ALTERARLOTE_PERM TO USER_LF
/